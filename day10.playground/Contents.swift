enum Delimiter {
  case squareBracket
  case curlyBracket
  case angleBracket
  case bracket

  init?(opener: Character) {
    switch opener {
    case "[":
      self = .squareBracket
    case "{":
      self = .curlyBracket
    case "<":
      self = .angleBracket
    case "(":
      self = .bracket
    default:
      return nil
    }
  }

  init?(closer: Character) {
    switch closer {
    case "]":
      self = .squareBracket
    case "}":
      self = .curlyBracket
    case ">":
      self = .angleBracket
    case ")":
      self = .bracket
    default:
      return nil
    }
  }

  var closer: Character {
    switch self {
    case .bracket:
      return ")"
    case .angleBracket:
      return ">"
    case .curlyBracket:
      return "}"
    case .squareBracket:
      return "]"
    }
  }

  var score: Int {
    switch self {
    case .bracket:
      return 3
    case .squareBracket:
      return 57
    case .curlyBracket:
      return 1197
    case .angleBracket:
      return 25137
    }
  }

  var points: Int {
    switch self {
    case .bracket:
      return 1
    case .squareBracket:
      return 2
    case .curlyBracket:
      return 3
    case .angleBracket:
      return 4
    }
  }
}


extension String {
  func removingClosedChunks() -> String {
    //remove closed chunks:
    var cleaned = self
    while cleaned.contains("[]") || cleaned.contains("<>") || cleaned.contains("{}") || cleaned.contains("()") {
      cleaned = cleaned.replacingOccurrences(of: "<>", with: "")
        .replacingOccurrences(of: "[]", with: "")
        .replacingOccurrences(of: "{}", with: "")
        .replacingOccurrences(of: "()", with: "")
    }
    return cleaned
  }

  var errorScore: Int? {
    let array = Array(removingClosedChunks())
    for character in array {
      if let delimiter = Delimiter(closer: character) {
        return delimiter.score
      }
    }
    return nil
  }
}
print(lines.compactMap { $0.errorScore}.reduce(0, +))



let test = "<{([{{}}[<[[[<>{}]]]>[]]"
print(test.removingClosedChunks())

func points(for line: String) -> Int {
  let points = line.removingClosedChunks().reversed().map { Delimiter(opener: $0)!.points }
  return points.reduce(0) { x, y in
    (x * 5) + y
  }
}

print(points(for: test))


let incompleteLines = lines.filter { $0.errorScore == nil }
let incompleteScores = incompleteLines.map { points(for: $0)}.sorted()
print(incompleteScores[incompleteScores.count/2])

